// 온도·강수량으로 날씨 아이콘/라벨 결정
// 우선순위: 눈 → 폭우/우기 → 비 → 폭염 → 더움 → 따뜻(꽃) → 쾌적 → 쌀쌀 → 추위
export function weatherIcon(temp: number, rain: number): {
  icon: string
  mood: string
} {
  if (temp <= 0 && rain >= 30) return { icon: '❄️', mood: '눈' }
  if (rain >= 250) return { icon: '⛈️', mood: '우기' }
  if (rain >= 150) return { icon: '🌧️', mood: '장맛비' }
  if (rain >= 90) return { icon: '🌦️', mood: '비잦음' }
  if (temp >= 30) return { icon: '🥵', mood: '폭염' }
  if (temp >= 26) return { icon: '☀️', mood: '더움' }
  if (temp >= 18) return { icon: '🌸', mood: '따사로움' }
  if (temp >= 10) return { icon: '🍃', mood: '쾌적' }
  if (temp >= 3) return { icon: '🧥', mood: '쌀쌀' }
  return { icon: '❄️', mood: '한파' }
}

// 여행 적합도 점수를 색상으로 매핑
// 90+ 에메랄드 (최적) → 30- 회색 (피해야)
export function scoreToColor(score: number): {
  bg: string
  text: string
  label: string
} {
  if (score >= 88)
    return { bg: 'bg-emerald-100', text: 'text-emerald-800', label: '최적' }
  if (score >= 75)
    return { bg: 'bg-green-100', text: 'text-green-800', label: '좋음' }
  if (score >= 60)
    return { bg: 'bg-lime-100', text: 'text-lime-800', label: '보통' }
  if (score >= 45)
    return { bg: 'bg-amber-100', text: 'text-amber-800', label: '주의' }
  if (score >= 30)
    return { bg: 'bg-orange-100', text: 'text-orange-800', label: '나쁨' }
  return { bg: 'bg-slate-200', text: 'text-slate-700', label: '피해야' }
}
